#!/usr/bin/env bash
# Regenerate examples/*.stl and examples/REPORT.md
#
# Only ships STLs that pass the support-removability gate (cleanup OK).
set -euo pipefail
cd "$(dirname "$0")"

mkdir -p examples
CELL=4

# Wipe previous example STLs so hard-to-remove demos cannot linger.
rm -f examples/*.stl

TREE_FLAGS=(
  --mode breakaway
  --support-style tree
  --support-cluster 14
  --support-tip-radius 0.12
  --support-tip-height 2.0
  --support-branch-angle 40
  --support-max-tips-per-trunk 6
  --support-safety-factor 3
)

# Ten soup shapes verified easy to clean (score 100, no rest-on-model / trapped trunks).
# Format: seed:density
EASY_SOUPS=(
  60:0.12
  98:0.12
  262:0.12
  299:0.12
  415:0.12
  552:0.12
  51:0.14
  178:0.14
  920:0.14
  944:0.14
)

generate_easy() {
  local seed="$1"
  local density="$2"
  local out="$3"
  local report
  # Explicit soup seeds often exit non-zero due to Life orphans even when
  # supports are easy to remove — that is OK for shipping examples.
  report="$(cargo run --release --quiet -- --pattern soup --seed "$seed" --density "$density" \
    -x 16 -y 16 -z 24 --cell "$CELL" \
    "${TREE_FLAGS[@]}" -o "$out" 2>/dev/null || true)"
  if [[ ! -f "$out" ]]; then
    echo "error: failed to write ${out}" >&2
    return 1
  fi
  if ! grep -q 'cleanup OK' <<<"$report"; then
    echo "error: seed ${seed} failed removability gate; refusing to ship" >&2
    rm -f "$out"
    return 1
  fi
  if grep -q 'HARD TO REMOVE' <<<"$report"; then
    echo "error: seed ${seed} marked HARD TO REMOVE; refusing to ship" >&2
    rm -f "$out"
    return 1
  fi
  printf '%s\n' "$report"
}

{
  echo "# Example printability reports"
  echo
  echo "Generated by \`./generate-examples.sh\` with \`--cell ${CELL}\`."
  echo
  echo "Only examples that pass the **support removability** gate are shipped"
  echo "(\`cleanup OK\`: no rest-on-model landings, no trapped trunks, score ≥ 70)."
  echo "Hard-to-remove demos (dense soups, glider cages) are intentionally omitted."
  echo

  echo "## Ten easy-to-clean soup shapes (tree supports)"
  echo
  for entry in "${EASY_SOUPS[@]}"; do
    seed="${entry%%:*}"
    density="${entry##*:}"
    out="examples/soup-easy-${seed}.stl"
    echo "Generating seed=${seed} density=${density}…" >&2
    report="$(generate_easy "$seed" "$density" "$out")"
    echo "### soup seed=${seed} density=${density}"
    echo '```'
    echo "$report"
    echo '```'
    echo
  done

} | tee examples/REPORT.md

rm -f /tmp/life-stl-discard.stl
ls -lh examples/*.stl
echo "Wrote examples/REPORT.md ($(ls examples/*.stl | wc -l) STL files)"
