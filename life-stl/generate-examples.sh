#!/usr/bin/env bash
# Regenerate examples/*.stl and examples/REPORT.md
#
# Every shipped STL must stay ACTIVE for its entire printed height
# (complexity gate: no still life / short oscillator before the top layer)
# and be self-supporting (gusset mode: nothing to remove, one piece).
set -euo pipefail
cd "$(dirname "$0")"

mkdir -p examples
CELL=4

# Wipe previous example STLs so rejected demos cannot linger.
rm -f examples/*.stl

run() {
  # run <outfile> <args...> → prints report; fails if gates fail.
  local out="$1"
  shift
  local report
  report="$(cargo run --release --quiet -- "$@" --cell "$CELL" -o "$out" 2>/dev/null || true)"
  if [[ ! -f "$out" ]]; then
    echo "error: failed to write ${out}" >&2
    return 1
  fi
  if grep -q 'TOO BORING' <<<"$report" || ! grep -q 'interesting OK' <<<"$report"; then
    echo "reject ${out}: failed complexity gate" >&2
    rm -f "$out"
    return 1
  fi
  if grep -q 'HARD TO REMOVE' <<<"$report"; then
    echo "reject ${out}: HARD TO REMOVE" >&2
    rm -f "$out"
    return 1
  fi
  if ! grep -q 'Life self-supporting: yes' <<<"$report"; then
    echo "reject ${out}: not one self-supporting piece" >&2
    rm -f "$out"
    return 1
  fi
  printf '%s\n' "$report"
}

section() {
  local title="$1"
  shift
  local out="$1"
  shift
  echo "Generating ${out}…" >&2
  local report
  report="$(run "$out" "$@")"
  echo "### ${title}"
  echo '```'
  echo "$report"
  echo '```'
  echo
}

{
  echo "# Example printability reports"
  echo
  echo "Generated by \`./generate-examples.sh\` with \`--cell ${CELL}\` (A1 Mini: 180×180×180 mm)."
  echo
  echo "All examples use **gusset mode** (default): every Life birth leans on its"
  echo "three B3 parents via small diagonal braces, so the whole stack is"
  echo "self-supporting, prints as one piece, and needs no support cleanup."
  echo
  echo "Every example must stay **active for its entire printed height** — patterns"
  echo "that settle into a still life or short oscillator partway up are rejected"
  echo "(they extrude a boring static tower above the interesting part)."
  echo

  section "Glider tower — 64×64×180 mm" examples/gusset-glider-tower.stl \
    --pattern glider -x 16 -y 16 -z 44 --mode gusset

  section "Acorn methuselah — 176×176×180 mm" examples/gusset-acorn.stl \
    --pattern acorn -x 44 -y 44 -z 44 --mode gusset

  section "Soup — 96×96×148 mm" examples/gusset-soup-mid.stl \
    --pattern soup --seed 0 --density 0.18 -x 24 -y 24 -z 36 --mode gusset

  section "Soup, A1 Mini max — 176×176×180 mm" examples/gusset-soup-a1max.stl \
    --pattern soup --seed 0 --density 0.16 -x 44 -y 44 -z 44 --mode gusset

} | tee examples/REPORT.md

shopt -s nullglob
stls=(examples/*.stl)
ls -lh "${stls[@]}"
echo "Wrote examples/REPORT.md (${#stls[@]} STL files)"
