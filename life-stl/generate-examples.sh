#!/usr/bin/env bash
# Regenerate examples/*.stl and examples/REPORT.md
set -euo pipefail
cd "$(dirname "$0")"

mkdir -p examples
CELL=4

# Drop retired demos so regenerating never leaves stale STLs around.
rm -f \
  examples/random-42-breakaway.stl \
  examples/tower-10x10x60-breakaway.stl \
  examples/soup-99-breakaway.stl

run() {
  cargo run --release --quiet -- "$@" 2>/dev/null || true
}

capture_report() {
  local label="$1"
  shift
  echo "### ${label}"
  echo '```'
  cargo run --release --quiet -- "$@" 2>/dev/null || true
  echo '```'
  echo
}

{
  echo "# Example printability reports"
  echo
  echo "Generated by \`./generate-examples.sh\` with \`--cell ${CELL}\`."
  echo
  echo "Default supports are **breakaway**. Tree style merges nearby tips onto"
  echo "shared trunks (Cura/Bambu-style); pillar style keeps one shaft per tip."
  echo "After snapping them off, Life must be face-connected to the bed (no orphans)."
  echo

  echo "## Soup with Cura-style tree supports (shared trunks)"
  echo
  run --quiet --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode breakaway \
    --support-style tree --support-cluster 24 --support-trunk-radius 1.2 \
    --support-radius 0.55 --support-tip-radius 0.3 --support-branch-angle 45 \
    -o examples/soup-99-tree.stl
  capture_report "soup seed=99 / breakaway tree" --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode breakaway \
    --support-style tree --support-cluster 24 --support-trunk-radius 1.2 \
    --support-radius 0.55 --support-tip-radius 0.3 --support-branch-angle 45 \
    -o /tmp/life-stl-discard.stl

  echo "## Soup with pillars (one shaft per tip — contrast)"
  echo
  run --quiet --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode breakaway \
    --support-style pillar --support-radius 0.5 --support-tip-radius 0.28 \
    -o examples/soup-99-pillars.stl
  capture_report "soup seed=99 / breakaway pillars" --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode breakaway \
    --support-style pillar --support-radius 0.5 --support-tip-radius 0.28 \
    -o /tmp/life-stl-discard.stl

  echo "## Glider with tree supports (moving pattern → orphans)"
  echo
  run --quiet --pattern glider --seed 0 -x 12 -y 12 -z 24 --cell "$CELL" --mode breakaway \
    --support-style tree --support-cluster 20 --support-trunk-radius 1.1 \
    -o examples/glider-tree.stl
  capture_report "glider / breakaway tree" --pattern glider --seed 0 -x 12 -y 12 -z 24 --cell "$CELL" --mode breakaway \
    --support-style tree --support-cluster 20 --support-trunk-radius 1.1 \
    -o /tmp/life-stl-discard.stl

} | tee examples/REPORT.md

rm -f /tmp/life-stl-discard.stl
# Remove renamed legacy filenames if present.
rm -f examples/glider-breakaway.stl
ls -lh examples/*.stl
echo "Wrote examples/REPORT.md"
