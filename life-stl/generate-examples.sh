#!/usr/bin/env bash
# Regenerate examples/*.stl and examples/REPORT.md
set -euo pipefail
cd "$(dirname "$0")"

mkdir -p examples
CELL=4

run() {
  cargo run --release --quiet -- "$@" 2>/dev/null || true
}

capture_report() {
  local label="$1"
  shift
  echo "### ${label}"
  echo '```'
  # Allow non-zero exit (unprintable explicit seeds still emit STL + report).
  cargo run --release --quiet -- "$@" 2>/dev/null || true
  echo '```'
  echo
}

{
  echo "# Example printability reports"
  echo
  echo "Generated by \`./generate-examples.sh\` with \`--cell ${CELL}\` (default)."
  echo
  echo "Scaffold is **fused filament**, not breakaway. **Life orphans** = voxels that"
  echo "would float/disconnect if scaffold were removed. Default \`random\` is a"
  echo "still-life garden (self-supporting). \`soup\` is chaotic Bernoulli Life."
  echo

  echo "## Still-life garden seed 42 (24×24×48, cell=${CELL}mm)"
  echo
  run --quiet --pattern random --seed 42 -x 24 -y 24 -z 48 --cell "$CELL" --mode scaffold \
    -o examples/random-42-scaffold.stl
  capture_report "garden seed=42 / scaffold" --pattern random --seed 42 -x 24 -y 24 -z 48 --cell "$CELL" --mode scaffold -o /tmp/life-stl-discard.stl

  echo "## Glider (12×12×24) — not self-supporting"
  echo
  run --quiet --pattern glider --seed 0 -x 12 -y 12 -z 24 --cell "$CELL" --mode scaffold \
    -o examples/glider-scaffold.stl
  capture_report "glider / scaffold" --pattern glider --seed 0 -x 12 -y 12 -z 24 --cell "$CELL" --mode scaffold -o /tmp/life-stl-discard.stl

  echo "## Soup seed 99 (raw vs scaffold) — orphans remain if scaffold removed"
  echo
  run --quiet --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode scaffold \
    -o examples/soup-99-scaffold.stl
  capture_report "soup seed=99 / scaffold" --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode scaffold -o /tmp/life-stl-discard.stl
  capture_report "soup seed=99 / raw" --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode raw -o /tmp/life-stl-discard.stl

  echo "## Tower 10×10×60 cm (cell=${CELL}mm, still-life garden, scaffold)"
  echo
  echo "\`--width-mm 100 --height-mm 100 --depth-mm 600 --cell ${CELL}\` → 25×25×150 cells."
  echo "Note: a Bambu A1 Mini (180 mm tall) cannot print 600 mm in one piece."
  echo
  run --quiet --pattern random --seed 42 \
    --width-mm 100 --height-mm 100 --depth-mm 600 --cell "$CELL" --mode scaffold \
    -o examples/tower-10x10x60-scaffold.stl
  capture_report "tower 10×10×60 cm / scaffold" --pattern random --seed 42 \
    --width-mm 100 --height-mm 100 --depth-mm 600 --cell "$CELL" --mode scaffold \
    -o /tmp/life-stl-discard.stl

} | tee examples/REPORT.md

rm -f /tmp/life-stl-discard.stl \
  examples/glider-raw.stl examples/random-42-raw.stl examples/rpento-scaffold.stl 2>/dev/null || true
ls -lh examples/*.stl
echo "Wrote examples/REPORT.md"
