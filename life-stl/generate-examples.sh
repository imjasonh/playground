#!/usr/bin/env bash
# Regenerate examples/*.stl and examples/REPORT.md
set -euo pipefail
cd "$(dirname "$0")"

mkdir -p examples
CELL=4

# Drop retired / renamed demos so regenerating never leaves stale STLs around.
rm -f \
  examples/random-42-breakaway.stl \
  examples/tower-10x10x60-breakaway.stl \
  examples/soup-99-breakaway.stl \
  examples/glider-breakaway.stl \
  examples/a1-mini-max-garden.stl

run() {
  cargo run --release --quiet -- "$@" 2>/dev/null || true
}

capture_report() {
  local label="$1"
  shift
  echo "### ${label}"
  echo '```'
  cargo run --release --quiet -- "$@" 2>/dev/null || true
  echo '```'
  echo
}

# Shared tree-support knobs used by demos (needle tips + physics auto-size).
TREE_FLAGS=(
  --mode breakaway
  --support-style tree
  --support-cluster 14
  --support-tip-radius 0.12
  --support-tip-height 2.0
  --support-branch-angle 40
  --support-max-tips-per-trunk 6
  --support-safety-factor 3
)

{
  echo "# Example printability reports"
  echo
  echo "Generated by \`./generate-examples.sh\` with \`--cell ${CELL}\`."
  echo
  echo "Tree supports use **shared trunks**, **needle tips**, and a simplified"
  echo "**beam/column physics** model that splits overloaded trunks and thickens"
  echo "shafts for print stability while keeping contacts snappable."
  echo

  echo "## Soup with Cura-style tree supports (shared trunks + physics)"
  echo
  run --quiet --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" \
    "${TREE_FLAGS[@]}" --support-trunk-radius 1.1 \
    -o examples/soup-99-tree.stl
  capture_report "soup seed=99 / breakaway tree" --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" \
    "${TREE_FLAGS[@]}" --support-trunk-radius 1.1 \
    -o /tmp/life-stl-discard.stl

  echo "## Soup with pillars (one shaft per tip — contrast)"
  echo
  run --quiet --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode breakaway \
    --support-style pillar --support-radius 0.55 --support-tip-radius 0.12 --support-tip-height 2.0 \
    -o examples/soup-99-pillars.stl
  capture_report "soup seed=99 / breakaway pillars" --pattern soup --seed 99 -x 16 -y 16 -z 24 --cell "$CELL" --mode breakaway \
    --support-style pillar --support-radius 0.55 --support-tip-radius 0.12 --support-tip-height 2.0 \
    -o /tmp/life-stl-discard.stl

  echo "## Glider with physics-split tree supports"
  echo
  # Tighter tip-per-trunk cap so the moving glider does not hang off one trunk.
  run --quiet --pattern glider --seed 0 -x 12 -y 12 -z 24 --cell "$CELL" \
    --mode breakaway --support-style tree --support-cluster 14 \
    --support-tip-radius 0.12 --support-tip-height 2.0 --support-branch-angle 40 \
    --support-max-tips-per-trunk 4 --support-safety-factor 3 \
    -o examples/glider-tree.stl
  capture_report "glider / breakaway tree" --pattern glider --seed 0 -x 12 -y 12 -z 24 --cell "$CELL" \
    --mode breakaway --support-style tree --support-cluster 14 \
    --support-tip-radius 0.12 --support-tip-height 2.0 --support-branch-angle 40 \
    --support-max-tips-per-trunk 4 --support-safety-factor 3 \
    -o /tmp/life-stl-discard.stl

  echo "## Twenty soup seeds with tree supports"
  echo
  for seed in $(seq 100 119); do
    out="examples/soup-tree-${seed}.stl"
    run --quiet --pattern soup --seed "$seed" -x 16 -y 16 -z 24 --cell "$CELL" \
      "${TREE_FLAGS[@]}" -o "$out"
    capture_report "soup seed=${seed} / breakaway tree" --pattern soup --seed "$seed" -x 16 -y 16 -z 24 --cell "$CELL" \
      "${TREE_FLAGS[@]}" -o /tmp/life-stl-discard.stl
  done

  echo "## Bambu A1 Mini max build volume (180×180×180 mm)"
  echo
  echo "A1 Mini build volume is **180³ mm** (not 700 mm³ — that would be ~9 mm on a side)."
  echo "Fills the cube at \`--cell 4\` (45×45×45 cells) with soup + tree supports."
  echo "Still-life gardens are intentionally not shipped as examples."
  echo
  run --quiet --pattern soup --seed 42 --density 0.22 \
    --width-mm 180 --height-mm 180 --depth-mm 180 --cell "$CELL" \
    "${TREE_FLAGS[@]}" -o examples/a1-mini-max-soup-tree.stl
  capture_report "A1 Mini max / soup tree" --pattern soup --seed 42 --density 0.22 \
    --width-mm 180 --height-mm 180 --depth-mm 180 --cell "$CELL" \
    "${TREE_FLAGS[@]}" -o /tmp/life-stl-discard.stl

} | tee examples/REPORT.md

rm -f /tmp/life-stl-discard.stl
ls -lh examples/*.stl | head -40
echo "…"
ls examples/*.stl | wc -l | xargs -I{} echo "{} STL files in examples/"
echo "Wrote examples/REPORT.md"
