#!/usr/bin/env bash
# Regenerate examples/*.stl and examples/REPORT.md
set -euo pipefail
cd "$(dirname "$0")"

mkdir -p examples

run() {
  # Metrics go to stdout; "wrote …" goes to stderr (silenced for clean REPORT.md).
  cargo run --release --quiet -- "$@" 2>/dev/null
}

capture_report() {
  local label="$1"
  shift
  echo "### ${label}"
  echo '```'
  # stdout is the metrics report; stderr has "wrote …"
  run "$@" 2>/dev/null
  echo '```'
  echo
}

{
  echo "# Example printability reports"
  echo
  echo "Generated by \`./generate-examples.sh\`."
  echo
  echo "**Unsupported overhang** = bottom-face area of solids with an empty cell"
  echo "directly below (\`count × cell²\` mm²). That is the surface most likely to"
  echo "need supports or bridging on an FDM printer. Scaffold mode targets 0."
  echo

  echo "## Glider (12×12×24, cell=2mm)"
  echo
  run --quiet --pattern glider -x 12 -y 12 -z 24 --cell 2 --mode scaffold \
    -o examples/glider-scaffold.stl
  run --quiet --pattern glider -x 12 -y 12 -z 24 --cell 2 --mode raw \
    -o examples/glider-raw.stl
  capture_report "glider / scaffold" --pattern glider -x 12 -y 12 -z 24 --cell 2 --mode scaffold -o /tmp/life-stl-discard.stl
  capture_report "glider / raw" --pattern glider -x 12 -y 12 -z 24 --cell 2 --mode raw -o /tmp/life-stl-discard.stl

  echo "## Random seed 42 (24×24×48, cell=2mm)"
  echo
  run --quiet --pattern random --seed 42 -x 24 -y 24 -z 48 --cell 2 --mode scaffold \
    -o examples/random-42-scaffold.stl
  run --quiet --pattern random --seed 42 -x 24 -y 24 -z 48 --cell 2 --mode raw \
    -o examples/random-42-raw.stl
  capture_report "random seed=42 / scaffold" --pattern random --seed 42 -x 24 -y 24 -z 48 --cell 2 --mode scaffold -o /tmp/life-stl-discard.stl
  capture_report "random seed=42 / raw" --pattern random --seed 42 -x 24 -y 24 -z 48 --cell 2 --mode raw -o /tmp/life-stl-discard.stl

  echo "## R-pentomino (20×20×60, cell=2mm)"
  echo
  run --quiet --pattern rpento -x 20 -y 20 -z 60 --cell 2 --mode scaffold \
    -o examples/rpento-scaffold.stl
  capture_report "rpento / scaffold" --pattern rpento -x 20 -y 20 -z 60 --cell 2 --mode scaffold -o /tmp/life-stl-discard.stl
  capture_report "rpento / raw" --pattern rpento -x 20 -y 20 -z 60 --cell 2 --mode raw -o /tmp/life-stl-discard.stl

} | tee examples/REPORT.md

rm -f /tmp/life-stl-discard.stl
ls -lh examples/*.stl
echo "Wrote examples/REPORT.md"
